package escape_sequences;

public class SpecialCharacters {
    public static void main(String[] args) {
        /*
            apple\banana
            I like "Sundays" and apple.
            My favorite fruits are "Kiwi" and "Orange"
         */

        System.out.println("apple\\\\orange");
        System.out.println("I like \"Sundays\" and apple.");
        System.out.println("My favorite fruits are \"Kiwi\" and \"Orange\"");

        System.out.println("My Favorite football team is \"DA BEARS\"");
    }



}
